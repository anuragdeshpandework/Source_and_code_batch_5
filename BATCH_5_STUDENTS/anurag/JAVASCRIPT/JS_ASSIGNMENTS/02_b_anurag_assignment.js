//create and declare a variable and assign string value to it and log on console
let userName = "anurag"
console.log(userName)   

//create and declare a variable and assign boolean value to it and log on console
let isStudent = true
console.log(isStudent)  

//create and declare a variable and assign undefined value to it and log on console
let undefinedVariable
console.log(undefinedVariable)

//create and declare a variable and assign null value to it and log on console
let nullVariable = null
console.log(nullVariable)   

//check the data type of each variable using typeof operator
console.log(typeof userName) // string
console.log(typeof isStudent) // boolean
console.log(typeof undefinedVariable) // undefined
console.log(typeof nullVariable) // object (this is a known quirk in JavaScript, null is considered an object)