/*
========================================================
VISUAL STUDIO CODE (VS CODE) – BASIC NOTES
========================================================
Visual Studio Code (VS Code) is one of the most popular
code editors used by developers for writing, debugging,
and managing code efficiently.
*/


/*
--------------------------------------------------------
1. What is VS Code?
--------------------------------------------------------

VS Code (Visual Studio Code) is a lightweight but powerful
source code editor developed by Microsoft.

It supports multiple programming languages and provides
many useful features for developers.

Official Website:
https://code.visualstudio.com

Commonly used for:
- Web Development
- JavaScript / TypeScript
- Python
- Automation Testing
- Node.js
- Git and GitHub integration
*/


/*
--------------------------------------------------------
2. Key Features of VS Code
--------------------------------------------------------

1. Lightweight and Fast
VS Code runs quickly and does not consume too much memory.

2. Extensions Support
Thousands of extensions are available to add new features.

3. Built-in Git Support
You can perform Git operations directly from VS Code.

4. IntelliSense
Provides smart suggestions and auto-completion while coding.

5. Debugging Support
Allows you to debug your code easily.

6. Integrated Terminal
You can run terminal commands directly inside VS Code.
*/


/*
--------------------------------------------------------
3. Installing VS Code
--------------------------------------------------------

Step 1
Visit the official website:

https://code.visualstudio.com

Step 2
Download the installer based on your operating system:
- Windows
- Mac
- Linux

Step 3
Run the installer and complete the installation process.

Step 4
Open VS Code from your system.
*/


/*
--------------------------------------------------------
4. Important VS Code UI Sections
--------------------------------------------------------

1. Explorer
Shows project files and folders.

Shortcut:
Ctrl + Shift + E

Use:
To navigate and manage files in the project.


2. Search
Search text across the entire project.

Shortcut:
Ctrl + Shift + F


3. Source Control
Manage Git repositories.

Shortcut:
Ctrl + Shift + G


4. Run & Debug
Run and debug applications.

Shortcut:
Ctrl + Shift + D


5. Extensions
Install and manage extensions.

Shortcut:
Ctrl + Shift + X
*/


/*
--------------------------------------------------------
5. Opening a Project in VS Code
--------------------------------------------------------

Method 1:
Using File Menu

File → Open Folder → Select Project Folder


Method 2:
Using Terminal

Navigate to the project folder and run:

code .


Example:

cd my-project
code .
*/


/*
--------------------------------------------------------
6. Integrated Terminal
--------------------------------------------------------

VS Code provides a built-in terminal for running commands.

Open Terminal:

Shortcut:
Ctrl + `

Menu:
Terminal → New Terminal

You can run commands like:

git status
npm install
node app.js
*/


/*
--------------------------------------------------------
7. Useful VS Code Shortcuts
--------------------------------------------------------

Open Command Palette
Ctrl + Shift + P

Open Terminal
Ctrl + `

Open File
Ctrl + P

Save File
Ctrl + S

Format Document
Shift + Alt + F

Close File
Ctrl + W
*/


/*
--------------------------------------------------------
8. Important Extensions for Developers
--------------------------------------------------------

1. Prettier
Formats code automatically.

2. ESLint
Helps identify JavaScript errors.

3. Live Server
Runs HTML projects with live reload.

4. GitLens
Enhances Git capabilities inside VS Code.

5. Path IntelliSense
Auto-completes file paths.
*/


/*
--------------------------------------------------------
9. VS Code + Git Workflow
--------------------------------------------------------

VS Code integrates with Git very well.

Common Git workflow inside VS Code:

1. Open project folder

2. Make code changes

3. Open Source Control panel

4. Stage changes

5. Write commit message

6. Commit changes

7. Push changes to GitHub
*/


/*
--------------------------------------------------------
10. Best Practices While Using VS Code
--------------------------------------------------------

1. Always install required extensions.

2. Use Git integration for version control.

3. Keep your workspace organized.

4. Use shortcuts to improve productivity.

5. Regularly format your code using Prettier.
*/


